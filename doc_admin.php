				<h2>Presentación </h2>
				<p>El doctorado en administración y dirección de empresas busca desarrollar en sus participantes la investigación, en los campos y en los ejes relacionados con la administración de empresas. El planeamiento y la dirección estratégica, los procesos de marketing y el ámbito financiero y de capitales, para generar conocimiento nuevo y esencialmente aplicativo a la resolución de los problemas que en la actualidad se presentan en el Perú. Este doctorado, permite al mismo tiempo desarrollar líneas de investigación que posibiliten la ampliación del aparato conceptual disponible, en el marco de una visión estratégica y del desarrollo, a través de la cual se pueda apreciar la transcendencia y significado personal, económico, político, social, cultural e histórico de las ciencias administrativas.</p>

				<h2>Objetivos: </h2>
