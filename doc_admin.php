				    <li>Formar investigadores con actitud crítica y creativa, suficiencia y solvencia profesional para generar conocimientos  científicos y dirigir con éxito diversos grupos académicos y empresariales multidisciplinarios y multiculturales para la investigación y el servicio en el campo de la administración y dirección de empresas.</li>
				    <li>Desarrollar holísticamente la visión humanística, amplia y sensible a los temas del pensamiento contemporáneo, con conocimiento  profundo de las áreas específicas de la administración y dirección de empresas.</li>
